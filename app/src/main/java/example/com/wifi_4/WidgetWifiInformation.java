package example.com.wifi_4;

public class WidgetWifiInformation {

    private int _netId;
    private String _ssid;
    private String _title;

    public int get_netId() {
        return _netId;
    }

    public void set_netId(int _netId) {
        this._netId = _netId;
    }

    public String get_ssid() {
        return _ssid;
    }

    public void set_ssid(String _ssid) {
        this._ssid = _ssid;
    }

    public String get_title() {
        return _title;
    }

    public void set_title(String _title) {
        this._title = _title;
    }
}
